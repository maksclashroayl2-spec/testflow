# 4. ПРОВЕРКА СТРУКТУРЫ ДО СОЗДАНИЯ
# =========================

single_count = sum(1 for q in questions_data if q["type"] == "single")
multiple_count = sum(1 for q in questions_data if q["type"] == "multiple")
text_count = sum(1 for q in questions_data if q["type"] == "text")

if len(questions_data) != 50:
    raise Exception(f"Должно быть 50 вопросов, сейчас: {len(questions_data)}")

if single_count != 30:
    raise Exception(f"Должно быть 30 вопросов с одним ответом, сейчас: {single_count}")

if multiple_count != 10:
    raise Exception(f"Должно быть 10 вопросов с двумя правильными ответами, сейчас: {multiple_count}")

if text_count != 10:
    raise Exception(f"Должно быть 10 текстовых вопросов, сейчас: {text_count}")

for q in questions_data:
    if q["type"] == "single":
        correct_count = sum(1 for _, is_correct in q["answers"] if is_correct)
        if correct_count != 1:
            raise Exception(f"В single-вопросе должен быть 1 правильный ответ: {q['text']}")

    if q["type"] == "multiple":
        correct_count = sum(1 for _, is_correct in q["answers"] if is_correct)
        if correct_count != 2:
            raise Exception(f"В multiple-вопросе должно быть 2 правильных ответа: {q['text']}")


# =========================
# 5. РАНДОМНЫЙ ПОРЯДОК ВОПРОСОВ
# =========================

random.shuffle(questions_data)


# =========================
# 6. СОЗДАНИЕ ВОПРОСОВ И ОТВЕТОВ
# =========================

for q_data in questions_data:
    if q_data["type"] == "text":
        Question.objects.create(
            test=test,
            text=q_data["text"],
            question_type="text",
            correct_text=q_data["correct_text"]
        )
    else:
        question = Question.objects.create(
            test=test,
            text=q_data["text"],
            question_type="multiple"
        )

        answers = q_data["answers"][:]
        random.shuffle(answers)

        for answer_text, is_correct in answers:
            Answer.objects.create(
                question=question,
                text=answer_text,
                is_correct=is_correct
            )


