if len(questions_data) != 60:
    raise Exception(f"Должно быть 60 вопросов, сейчас: {len(questions_data)}")

if single_count != 40:
    raise Exception(f"Должно быть 40 вопросов с одним правильным ответом, сейчас: {single_count}")

if multiple_count != 10:
    raise Exception(f"Должно быть 10 вопросов с двумя правильными ответами, сейчас: {multiple_count}")

if text_count != 10:
    raise Exception(f"Должно быть 10 текстовых вопросов, сейчас: {text_count}")

for q in questions_data:
    if q["type"] == "single":
        correct_count = sum(1 for _, is_correct in q["answers"] if is_correct)
        if correct_count != 1:
            raise Exception(f"В single-вопросе должен быть 1 правильный ответ: {q['text']}")

    if q["type"] == "multiple":
        correct_count = sum(1 for _, is_correct in q["answers"] if is_correct)
        if correct_count != 2:
            raise Exception(f"В multiple-вопросе должно быть 2 правильных ответа: {q['text']}")


