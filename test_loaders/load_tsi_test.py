def add_choice(text, answers):
    question = Question.objects.create(
        test=test,
        text=text,
        question_type="multiple"
    )

    answers_copy = answers[:]
    random.shuffle(answers_copy)

    for answer_text, is_correct in answers_copy:
        Answer.objects.create(
            question=question,
            text=answer_text,
            is_correct=is_correct
        )


def add_text(text, correct_text):
    Question.objects.create(
        test=test,
        text=text,
        question_type="text",
        correct_text=correct_text
    )


